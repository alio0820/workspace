	public Map(){
		for(int i = 0; i < map.length; i++){
			for(int j = 0; j < map[i].length; j++){
				if(map[i][j] == 1){
					stone = new Stone(j, i);
					stones.add(stone);
					System.out.println("stone.x = " + stone.getX() + ", stone.y = " + stone.getY());
				}
			}
		}
	}
	
	public void drawMe(Graphics g){
		for(Stone s : stones){
			s.drawMe(g);
		}
	}
	
	public boolean isSnakeHitStone(Snake snake){
		boolean flag = false;
		for(Stone s : stones){
			if(s.isHitMe(snake)){
				flag = true;
				break;
			}
		}
		return flag;
	}

