package swu.edu.cn.bean;

public class DepartmentBean {
	
	private String departmenID;
	private String departmentName;
	private String departmentClassify;
	private String comment;
	

	public String getDepartmenID() {
		return departmenID;
	}
	public void setDepartmenID(String departmenID) {
		this.departmenID = departmenID;
	}
	
	public String getDepartmentName() {
		return departmentName;
	}
	public void setDepartmentName(String departmentName) {
		this.departmentName = departmentName;
	}
	
	public String getDepartmentClassify() {
		return departmentClassify;
	}
	public void setDepartmentClassify(String departmentClassify) {
		this.departmentClassify = departmentClassify;
	}
	
	public String getComment() {
		return comment;
	}
	public void setComment(String comment) {
		this.comment = comment;
	}
	

}
